# Get user input in celsius
celsius = float(input("Enter temperature in celsius:  "))

# Output/conversion of fahrenheit
fahrenheit = (9/5)*(celsius+32)

# Final calculation
print("Inputted celsius: " + str(celsius))
print("Celsius to fahrenheit: " + str(fahrenheit))
